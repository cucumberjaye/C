#include "libft.h"

void	*ft_bzero(void *buf, size_t count)
{
	size_t	i;
	char	*str;

	str = (char *)buf;
	i = 0;
	while (i < count)
	{
		str[i] = '\0';
		++i;
	}
	return (buf);
}
